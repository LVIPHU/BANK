CREATE PROC [dbo].[SP_CHECKCUSHAVEACCOUNTS]
@CMND NCHAR(10)
AS
BEGIN
	
	IF EXISTS(SELECT 1 FROM TaiKhoan
				WHERE TaiKhoan.CMND = @CMND)
	BEGIN
		RETURN 1;
	END

	RETURN 0;
END
GO

